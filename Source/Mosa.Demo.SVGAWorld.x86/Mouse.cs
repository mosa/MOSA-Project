		public static bool IsInBounds(int x, int y, int width, int height)
		{
			return Display.IsInBounds(X, x, Y, y, width, height);
		}

		public static void Draw()
		{
			if (!IsOnPaintingArea)
				for (var x = 0; x < 20; x++)
					for (var y = 0; y < 20; y++)
					{
						var color = MouseCols[y][x];

						if (color != 0)
							Display.DrawPoint(X + x, Y + y, Color);
					}
				//Display.Driver.SetCursor(true, (uint)X, (uint)Y);
			else
				Display.DrawPoint(X, Y, Color);
		}
